// if element in NxM matrix is 1, make it's row and column all 1 as well


func markRowAndColumn(input: [[Int]]) -> [[Int]] {
    
    var matrix: [[Int]] = input
    let M: Int = matrix.first!.count
    let N: Int = matrix.count
    var coordinates: [[Int]] = []

    
    // find out "1" coordinates
    for y in 0..<N {
        for x in 0..<M {
            if matrix[y][x] == 1 {
                coordinates.append([x, y])
            }
        }
    }
    
    // mark rows and columns with "1"
    for coord in coordinates {
        let x = coord.first!
        let y = coord.last!
        
        //mark row with "1"
        for i in 0..<M {
            matrix[y][i] = 1
        }
        
        // mark column with "1"
        for j in 0..<N {
            matrix[j][x] = 1
        }
        
    }
    
    return matrix
}


